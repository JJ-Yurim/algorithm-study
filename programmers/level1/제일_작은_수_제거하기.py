def solution(arr):

    answer = []
    smallest = min(arr) 

    for a in arr:
        if a != smallest:
            answer.append(a)
    
    if len(arr) == 1:
        answer.append(-1)

    return answer

arr = [4,3,2,1]
# arr = [10]

print(solution(arr))



